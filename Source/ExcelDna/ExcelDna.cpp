// Minimal parts of XLOPER types, 
// used only for xlAddInManagerInfo(12). Really.
struct XLOPER
{
	union
	{
		double num;					/* xltypeNum */
		void* str;					/* xltypeStr */
		WORD err;					/* xltypeErr */
	} val;
	WORD xltype; // Should be at offset 8 bytes
};

struct XLOPER12
{
	union
	{
		double num;					/* xltypeNum */
		void* str;					/* xltypeStr */
		int err;					/* xltypeErr */
		struct
		{
			double unused1;
			double unused2;
			double unused3;
		} unused;
	} val;
	DWORD xltype; // Should be at offset 24 bytes
};
#define xltypeNum  1;
#define xltypeStr  2;
#define xltypeErr  16;
#define xlerrValue 15

// The one and only ExportInfo
